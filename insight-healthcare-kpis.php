                        <span class="text-sky font-bold mt-1">•</span>
                        <span><strong>Mortality Rates:</strong> Risk-adjusted mortality for specific conditions</span>
                    </li>
                    <li class="flex gap-3">
                        <span class="text-sky font-bold mt-1">•</span>
                        <span><strong>Infection Rates:</strong> Hospital-acquired infection (HAI) metrics</span>
