                        <span class="text-sky font-bold mt-1">•</span>
                        <span><strong>Medication Safety:</strong> Adverse drug event rates</span>
                    </li>
                </ul>

                <h2 class="text-3xl font-bold text-navy mb-6">3. Operational Efficiency</h2>
                <p class="text-medium-text leading-relaxed mb-4">
                    Efficient operations reduce costs and improve capacity:
                </p>
                <ul class="space-y-3 mb-8 text-medium-text">
                    <li class="flex gap-3">
                        <span class="text-sky font-bold mt-1">•</span>
                        <span><strong>Length of Stay (LOS):</strong> Average number of days patients stay in the facility</span>
                    </li>
                    <li class="flex gap-3">
                        <span class="text-sky font-bold mt-1">•</span>
                        <span><strong>Bed Utilization Rate:</strong> Percentage of available beds in use</span>
                    </li>
                    <li class="flex gap-3">
                        <span class="text-sky font-bold mt-1">•</span>
                        <span><strong>Operating Room Efficiency:</strong> Percentage of scheduled OR time utilized</span>
                    </li>
                    <li class="flex gap-3">
                        <span class="text-sky font-bold mt-1">•</span>
                        <span><strong>Staff Turnover Rate:</strong> Annual percentage of staff departures</span>
                    </li>
                </ul>

                <h2 class="text-3xl font-bold text-navy mb-6">4. Financial Performance</h2>
                <p class="text-medium-text leading-relaxed mb-4">
                    Financial sustainability ensures long-term organizational health:
                </p>
                <ul class="space-y-3 mb-8 text-medium-text">
                    <li class="flex gap-3">
                        <span class="text-sky font-bold mt-1">•</span>
                        <span><strong>Operating Margin:</strong> Percentage of revenue remaining after operating expenses</span>
                    </li>
                    <li class="flex gap-3">
                        <span class="text-sky font-bold mt-1">•</span>
                        <span><strong>Case Mix Index:</strong> Measurement of relative complexity of cases</span>
                    </li>
                    <li class="flex gap-3">
                        <span class="text-sky font-bold mt-1">•</span>
                        <span><strong>Accounts Receivable Days:</strong> Average time to collect payment</span>
                    </li>
                    <li class="flex gap-3">
                        <span class="text-sky font-bold mt-1">•</span>
                        <span><strong>Cost per Discharge:</strong> Total costs divided by number of patient discharges</span>
                    </li>
                </ul>

